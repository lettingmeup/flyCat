	private String comName;

	private Long comPrice;
	
	private String fruitNum;

	public Long getCommondityId() {
		return commondityId;
	}

	public void setCommondityId(Long commondityId) {
		this.commondityId = commondityId;
	}

	public String getComName() {
		return comName;
	}

